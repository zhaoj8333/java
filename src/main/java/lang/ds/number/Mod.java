package lang.ds.number;

public class Mod {
    public static void main(String[] args) {
        mod();
    }

    public static void mod() {

        int a = 14;
        int b = -3;
        System.out.println(a % b); // 2

        a = -14;
        b = 3;
        System.out.println(a % b); // -2
    }
}
